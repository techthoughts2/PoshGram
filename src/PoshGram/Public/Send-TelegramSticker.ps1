            ':women_with_bunny_ears:',
            ':women_wrestling:',
            ':women''s_room:',
            ':wood:',
            ':woozy_face:',
            ':world_map:',
            ':worm:',
            ':worried_face:',
            ':wrapped_gift:',
            ':wrench:',
            ':writing_hand:',
            ':writing_hand:_dark_skin_tone:',
            ':writing_hand:_light_skin_tone:',
            ':writing_hand:_medium_skin_tone:',
            ':writing_hand:_medium-dark_skin_tone:',
            ':writing_hand:_medium-light_skin_tone:',
            ':x-ray:',
            ':yarn:',
